##-----------------------------------------------------------------------------


##-----------------------------------------------------------------------------
## Data load
##-----------------------------------------------------------------------------
load("datamatrix.txt")
load("phenodata.txt")
##-----------------------------------------------------------------------------


##-----------------------------------------------------------------------------
## Time series analysis
##-----------------------------------------------------------------------------
# Make design matrix
design <- make.design.matrix(phenodata)

NBp <- p.vector(datamatrix, design, counts = TRUE)
NBt <- T.fit(NBp)
get <- get.siggenes(NBt, vars = "all")
##-----------------------------------------------------------------------------


##-----------------------------------------------------------------------------
## Results plots
##-----------------------------------------------------------------------------
suma2Venn(get$summary[, c(2:4)])
